def _is_mirror_reachable(mirror: str, timeout: float = 3.0) -> bool:
    """Quick TCP connectivity check for a Docker registry mirror.

    Avoids wasting minutes on pull retries against unreachable mirrors.
    """
    if not mirror:
        return True  # Docker Hub — reachability depends on daemon proxy, skip check
    try:
        parsed = urlparse(f"https://{mirror}")
        host = parsed.hostname or mirror
        port = parsed.port or 443
        with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as s:
            s.settimeout(timeout)
            s.connect((host, port))
            return True
    except (ConnectionRefusedError, TimeoutError, OSError):
        return False


def _pull_image_with_mirrors(image: str) -> bool:
    """尝试从多个镜像源拉取单个镜像，每个源最多重试 PULL_MAX_RETRIES 次。

    对每个 mirror 先做 TCP 连通性预检（3s），不可达则直接跳过，
    避免在已失效的镜像源上浪费拉取超时。
    成功拉取后，若使用的是 mirror，会 tag 回原始镜像名以保证 compose 可用。
    """
    for mirror in DOCKER_REGISTRY_MIRRORS:
        source_label = mirror if mirror else "Docker Hub"
        # Quick reachability check for third-party mirrors
        if mirror and not _is_mirror_reachable(mirror):
            info(f"  [{source_label}] 不可达，跳过")
            continue
        rewritten = _rewrite_image(image, mirror)
        for attempt in range(1, PULL_MAX_RETRIES + 1):
            info(
                f"  [{source_label}] 拉取 {rewritten} (第 {attempt}/{PULL_MAX_RETRIES} 次)..."
            )
            try:
                result = subprocess.run(
                    ["docker", "pull", rewritten],
                    capture_output=True,
                    text=True,
                    timeout=300,
                )
                if result.returncode == 0:
                    ok(f"  {image} <- {source_label}")
                    # mirror 拉取成功后 tag 回原始名称
                    if mirror:
                        subprocess.run(
                            ["docker", "tag", rewritten, image],
                            capture_output=True,
                            timeout=30,
                        )
                    return True
            except subprocess.TimeoutExpired:
                warn(f"  [{source_label}] 拉取超时")
            except OSError as e:
                warn(f"  [{source_label}] 拉取异常: {e}")
        # 当前 mirror 全部重试失败，尝试下一个
        if mirror:
            info(f"  {source_label} 拉取失败，切换下一个镜像源...")
    return False


def _pull_all_images(compose_file: str) -> bool:
    """从 compose 文件中提取所有镜像并逐个拉取 (带镜像源重试)。

    返回 True 表示全部成功，False 表示存在失败 (但不阻断流程)。
    """
    # 从 compose 文件解析 image 字段
    images = []
    try:
        with open(compose_file, "r", encoding="utf-8") as f:
            for line in f:
                stripped = line.strip()
                if stripped.startswith("image:"):
                    img = stripped.split(":", 1)[1].strip().strip('"').strip("'")
                    # 去除行尾 YAML 注释
                    if "  #" in img:
                        img = img[: img.index("  #")].rstrip()
                    if img:
                        images.append(img)
    except OSError:
        warn(f"无法读取 compose 文件: {compose_file}")
        return False

    if not images:
        warn("未在 compose 文件中找到任何镜像定义")
        return False

    info(f"共需拉取 {len(images)} 个镜像")
    all_ok = True
    for image in images:
        if not _pull_image_with_mirrors(image):
            warn(f"  {image} 所有镜像源均失败")
            all_ok = False
    return all_ok


def deploy_penpot(config: dict) -> bool:
    """通过 Docker Compose 部署 Penpot。依赖已由 preflight_check 确认。"""
    section("部署 Penpot (Docker Compose)")

    # 确保 Docker daemon 运行（自动启动或安装）
    if not _ensure_docker_running():
        return False

    # 检查 Penpot 是否已在运行 (Docker 容器 + 端口双重验证)
    if _is_penpot_running(config):
        ok("Penpot 已在运行，跳过部署")
        return True

    # 端口可用性检测，冲突时自动切换
    orig_port = config["penpot_port"]
    config["penpot_port"] = find_available_port(orig_port, "Penpot Frontend")
    port_changed = config["penpot_port"] != orig_port

    # 获取 Docker Compose 命令 (已由 preflight_check 确认可用)
    dc_cmd = _docker_compose_cmd()

    # 生成 compose 文件 (端口变更时强制重新生成)
    compose_file = _generate_compose_file(config, force=port_changed)
    compose_dir = os.path.dirname(compose_file)

    # 配置 Docker daemon 代理 (使 docker pull 能走代理)
    _ensure_docker_proxy()

    # 拉取镜像 (多镜像源 + 重试)
    info("拉取 Docker 镜像 (首次可能需要几分钟)...")
    if not _pull_all_images(compose_file):
        warn("部分镜像拉取失败，尝试继续启动...")

    # 启动服务
    info("启动 Penpot 服务...")
    up_result = subprocess.run(
        dc_cmd + ["-f", compose_file, "up", "-d"],
        cwd=compose_dir,
        timeout=120,
    )
    if up_result.returncode != 0:
        fail("Docker Compose 启动失败")
        return False

    # 等待 Penpot 就绪
    info(f"等待 Penpot 就绪 (最多 {HEALTH_TIMEOUT}s)...")
    for i in range(HEALTH_TIMEOUT):
        if _is_penpot_running(config):
            ok(f"Penpot 就绪 {DIM}-> http://localhost:{config['penpot_port']}{NC}")
            return True
        time.sleep(1)
        if (i + 1) % 10 == 0:
            info(f"  已等待 {i + 1}s...")

    warn(f"Penpot 在 {HEALTH_TIMEOUT}s 内未就绪，可能仍在初始化中")
    info(f"请手动检查: docker compose -f {compose_file} logs -f")
    return True  # 不阻断流程，可能只是启动慢


