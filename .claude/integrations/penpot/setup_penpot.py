def cmd_status(config: dict):
    """显示服务状态。"""
    print(f"\n{CYAN}{BOLD}  Penpot 服务状态{NC}\n")

    # Penpot
    if _is_penpot_running(config):
        ok(f"Penpot Frontend  {DIM}-> http://localhost:{config['penpot_port']}{NC}")
    else:
        fail(f"Penpot Frontend  (端口 {config['penpot_port']} 未监听)")

    # Docker Compose 详情
    compose_file = os.path.join(config["penpot_dir"], "docker-compose.yml")
    if os.path.isfile(compose_file):
        dc_cmd = _docker_compose_cmd()
        if dc_cmd:
            result = run_cmd(
                dc_cmd + ["-f", compose_file, "ps", "--format", "json"],
                cwd=config["penpot_dir"],
                timeout=10,
            )
            if result.returncode == 0 and result.stdout.strip():
                info("Docker 容器状态:")
                # docker compose ps --format json 可能输出多行 JSON
                for line in result.stdout.strip().splitlines():
                    try:
                        svc = json.loads(line)
                        name = svc.get("Name", svc.get("name", "?"))
                        state = svc.get("State", svc.get("state", "?"))
                        status_str = svc.get("Status", svc.get("status", ""))
                        marker = (
                            f"{GREEN}OK{NC}"
                            if state == "running"
                            else f"{RED}{state}{NC}"
                        )
                        print(f"    [{marker}] {name} {DIM}{status_str}{NC}")
                    except json.JSONDecodeError:
                        pass

    # MCP Server
    if _is_mcp_running(config):
        ok(f"MCP Server       {DIM}-> http://localhost:{config['mcp_port']}/mcp{NC}")
    else:
        fail(f"MCP Server       (端口 {config['mcp_port']} 未监听)")

    pid = _read_mcp_pid()
    if pid:
        alive = _is_process_alive(pid)
        if alive:
            info(f"MCP PID: {pid} (存活)")
        else:
            warn(f"MCP PID: {pid} (已失效，清理 PID 文件)")
            _remove_mcp_pid()

    # Plugin Server
    if is_port_listening(config["plugin_port"]):
        ok(f"Plugin Server    {DIM}-> http://localhost:{config['plugin_port']}{NC}")
    else:
        info(f"Plugin Server    (端口 {config['plugin_port']} 未监听)")

    print()
    return 0


def cmd_ensure(config: dict):
    """快速模式: 已运行则退出，否则启动。"""
    # MCP 已运行
    if _is_mcp_running(config):
        print(f"Penpot MCP already running on port {config['mcp_port']}")
        return 0

    # Penpot 和 MCP 都未运行，尝试启动
    compose_file = os.path.join(config["penpot_dir"], "docker-compose.yml")
    if os.path.isfile(compose_file):
        # 确保 Docker daemon 在运行
        if has_command("docker") and not _ensure_docker_running():
            fail("Docker daemon 无法启动，请手动启动 Docker Desktop")
            return 1
        dc_cmd = _docker_compose_cmd()
        if dc_cmd and not _is_penpot_running(config):
            subprocess.run(
                dc_cmd + ["-f", compose_file, "up", "-d"],
                cwd=config["penpot_dir"],
                capture_output=True,
                timeout=120,
            )
            # 等待 Penpot
            for _ in range(30):
                if _is_penpot_running(config):
                    break
                time.sleep(1)

    # 启动 MCP
    if start_mcp(config):
        return 0

    print(f"Penpot MCP not installed. Run: python {__file__} deploy", file=sys.stderr)
    return 1


