def _read_mcp_pid() -> Optional[int]:
    """读取 MCP Server PID。"""
    if not os.path.isfile(MCP_PID_FILE):
        return None
    try:
        with open(MCP_PID_FILE, "r") as f:
            return int(f.read().strip())
    except (ValueError, OSError):
        return None


def _write_mcp_pid(pid: int):
    """写入 MCP Server PID。"""
    with open(MCP_PID_FILE, "w") as f:
        f.write(str(pid))


def _remove_mcp_pid():
    """移除 PID 文件。"""
    try:
        os.remove(MCP_PID_FILE)
    except OSError:
        pass


def _is_mcp_running(config: dict) -> bool:
    """检查 MCP Server 是否在运行。"""
    return is_port_listening(config["mcp_port"])


def _is_process_alive(pid: int) -> bool:
    """检查进程是否存活。"""
    try:
        if PLATFORM == "windows":
            result = subprocess.run(
                ["tasklist", "/FI", f"PID eq {pid}"],
                capture_output=True, text=True, timeout=5,
            )
            return str(pid) in result.stdout
        else:
            os.kill(pid, 0)
            return True
    except (OSError, subprocess.TimeoutExpired):
        return False


def start_mcp(config: dict) -> bool:
    """启动 MCP Server (via npx @penpot/mcp@stable)。"""
    section("启动 MCP Server")

    # 检查 Node.js
    if not has_command("node"):
        fail("Node.js 未安装。请安装: https://nodejs.org (v18+)")
        return False
    node_ver = get_command_version(["node", "--version"])
    ok(f"Node.js {DIM}({node_ver}){NC}")

    # 检查 npx
    if not has_command("npx"):
        fail("npx 未找到，请确保 Node.js 安装正确")
        return False

    # 检查端口
    if _is_mcp_running(config):
        ok(f"MCP Server 已在端口 {config['mcp_port']} 运行")
        return True

    if not check_port_available(config["mcp_port"], "MCP Server"):
        return False

    # 构建环境变量
    env = os.environ.copy()
    env["PORT"] = str(config["mcp_port"])
    env["PLUGIN_PORT"] = str(config["plugin_port"])
    env["PENPOT_BASE_URL"] = f"http://localhost:{config['penpot_port']}"

    # 通过 npx 启动 (官方推荐的一行式启动)
    info("通过 npx @penpot/mcp@stable 启动 MCP Server...")
    cmd = ["npx", "@penpot/mcp@stable"]

    log_fh = open(MCP_LOG_FILE, "w", encoding="utf-8")

    if PLATFORM == "windows":
        # Windows: 使用 CREATE_NEW_PROCESS_GROUP 使子进程独立
        proc = subprocess.Popen(
            cmd,
            stdout=log_fh,
            stderr=subprocess.STDOUT,
            env=env,
            creationflags=subprocess.CREATE_NEW_PROCESS_GROUP,
        )
    else:
        # Unix: 使用 start_new_session 脱离终端
        proc = subprocess.Popen(
            cmd,
            stdout=log_fh,
            stderr=subprocess.STDOUT,
            env=env,
            start_new_session=True,
        )

    _write_mcp_pid(proc.pid)
    info(f"MCP Server PID: {proc.pid}, 日志: {MCP_LOG_FILE}")

    # 等待就绪
    for i in range(MCP_HEALTH_TIMEOUT):
        if _is_mcp_running(config):
            ok(f"MCP Server 就绪 {DIM}-> http://localhost:{config['mcp_port']}/mcp{NC}")
            return True
        # 检查进程是否已退出
        if proc.poll() is not None:
            fail(f"MCP Server 进程已退出 (exit={proc.returncode})")
            info(f"查看日志: {MCP_LOG_FILE}")
            _remove_mcp_pid()
            log_fh.close()
            return False
        time.sleep(1)

    warn(f"MCP Server 在 {MCP_HEALTH_TIMEOUT}s 内未就绪")
    info(f"查看日志: {MCP_LOG_FILE}")
    return True  # 不阻断，可能启动较慢


def stop_mcp(config: dict) -> bool:
    """停止 MCP Server。"""
    pid = _read_mcp_pid()

    # 尝试通过 PID 停止
    if pid and _is_process_alive(pid):
        info(f"停止 MCP Server (PID: {pid})...")
        try:
            if PLATFORM == "windows":
                subprocess.run(
                    ["taskkill", "/F", "/PID", str(pid)],
                    capture_output=True, timeout=10,
                )
            else:
                os.kill(pid, signal.SIGTERM)
                # 等待进程退出
                for _ in range(10):
                    if not _is_process_alive(pid):
                        break
                    time.sleep(0.5)
                else:
                    os.kill(pid, signal.SIGKILL)
        except OSError:
            pass
        _remove_mcp_pid()
        ok("MCP Server 已停止")
        return True

    # PID 无效但端口仍在监听
    if _is_mcp_running(config):
        warn(f"端口 {config['mcp_port']} 仍有服务，但无 PID 记录")
        info("请手动停止占用该端口的进程")
        _remove_mcp_pid()
        return False

    info("MCP Server 未在运行")
    _remove_mcp_pid()
    return True


