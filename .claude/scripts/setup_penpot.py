def main():
    ensure_utf8_stdio()

    # 加载 .env
    load_dotenv(override=True)

    parser = argparse.ArgumentParser(
        description="CataForge Penpot 完整部署工具",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=textwrap.dedent("""\
            子命令:
              deploy     完整部署: Penpot (Docker) + MCP Server (npx)
              mcp-only   仅启动 MCP Server
              start      启动已部署的服务
              stop       停止所有服务
              status     查看服务状态

            快速模式:
              --ensure   检测已运行则退出，否则启动
        """),
    )
    parser.add_argument(
        "command",
        nargs="?",
        choices=["deploy", "mcp-only", "start", "stop", "status"],
        help="子命令",
    )
    parser.add_argument(
        "--ensure",
        action="store_true",
        help="快速模式: 已运行则退出，否则启动",
    )

    args = parser.parse_args()
    config = _get_config()

    if args.ensure:
        sys.exit(cmd_ensure(config))

    if not args.command:
        parser.print_help()
        sys.exit(0)

    handlers = {
        "deploy": cmd_deploy,
        "mcp-only": cmd_mcp_only,
        "start": cmd_start,
        "stop": cmd_stop,
        "status": cmd_status,
    }

    sys.exit(handlers[args.command](config))


if __name__ == "__main__":
    main()
