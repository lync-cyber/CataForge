def register_claude_mcp(config: dict):
    """注册 Penpot MCP 到 Claude Code。"""
    section("注册 MCP 到 Claude Code")

    mcp_url = f"http://localhost:{config['mcp_port']}/mcp"

    if not has_command("claude"):
        info("Claude Code CLI 未检测到，请手动注册:")
        print(f"\n    {BOLD}claude mcp add penpot -t http {mcp_url}{NC}\n")
        return

    # 检查是否已注册
    result = run_cmd(["claude", "mcp", "list"], timeout=10)
    if result.returncode == 0 and "penpot" in result.stdout.lower():
        ok("Claude Code 已注册 penpot MCP")
        return

    # 注册
    info("注册 Penpot MCP Server...")
    result = run_cmd(["claude", "mcp", "add", "penpot", "-t", "http", mcp_url], timeout=10)
    if result.returncode == 0 or "Added" in (result.stdout or "") or "already" in (result.stdout or ""):
        ok("已注册到 Claude Code")
    else:
        warn(f"自动注册失败 (exit={result.returncode})")
        info(f"请手动执行: claude mcp add penpot -t http {mcp_url}")


