def cmd_deploy(config: dict):
    """完整部署: Penpot + MCP。"""
    print(f"\n{CYAN}{BOLD}  Penpot 完整部署 (Docker + MCP){NC}")
    print(f"  {'=' * 44}")
    print(f"  {DIM}Penpot: Docker Compose -> localhost:{config['penpot_port']}{NC}")
    print(f"  {DIM}MCP:    npx @penpot/mcp@stable -> localhost:{config['mcp_port']}{NC}")
    print()

    # Step 1: 部署 Penpot
    if not deploy_penpot(config):
        fail("Penpot 部署失败")
        return 1

    # Step 2: 启动 MCP
    if not start_mcp(config):
        warn("MCP Server 启动异常，但 Penpot 本体已部署")

    # Step 3: 注册到 Claude Code
    register_claude_mcp(config)

    # 完成摘要
    _print_summary(config)
    return 0


def cmd_mcp_only(config: dict):
    """仅启动 MCP Server。"""
    print(f"\n{CYAN}{BOLD}  Penpot MCP Server 部署{NC}")
    print(f"  {'=' * 44}\n")

    # 检查 Penpot 是否在运行
    if not _is_penpot_running(config):
        warn(f"Penpot 未在端口 {config['penpot_port']} 运行")
        info("MCP Server 需要连接到 Penpot 实例才能工作")
        info(f"请先运行: python {__file__} deploy")

    if not start_mcp(config):
        return 1

    register_claude_mcp(config)
    _print_summary(config, penpot_deployed=False)
    return 0


def cmd_start(config: dict):
    """启动已部署的服务。"""
    print(f"\n{CYAN}{BOLD}  启动 Penpot 服务{NC}\n")

    # 启动 Docker Compose
    compose_file = os.path.join(config["penpot_dir"], "docker-compose.yml")
    if os.path.isfile(compose_file):
        dc_cmd = _docker_compose_cmd()
        if dc_cmd:
            section("启动 Penpot (Docker)")
            result = subprocess.run(
                dc_cmd + ["-f", compose_file, "up", "-d"],
                cwd=config["penpot_dir"],
                timeout=120,
            )
            if result.returncode == 0:
                ok("Penpot Docker 服务已启动")
            else:
                fail("Penpot Docker 启动失败")
    else:
        info("未找到 Penpot Docker 部署，跳过")

    # 启动 MCP
    start_mcp(config)
    return 0


def cmd_stop(config: dict):
    """停止所有服务。"""
    print(f"\n{CYAN}{BOLD}  停止 Penpot 服务{NC}\n")

    # 停止 MCP
    section("停止 MCP Server")
    stop_mcp(config)

    # 停止 Docker Compose
    compose_file = os.path.join(config["penpot_dir"], "docker-compose.yml")
    if os.path.isfile(compose_file):
        dc_cmd = _docker_compose_cmd()
        if dc_cmd:
            section("停止 Penpot (Docker)")
            result = subprocess.run(
                dc_cmd + ["-f", compose_file, "down"],
                cwd=config["penpot_dir"],
                timeout=120,
            )
            if result.returncode == 0:
                ok("Penpot Docker 服务已停止")
            else:
                fail("Penpot Docker 停止失败")

    return 0


def cmd_status(config: dict):
    """显示服务状态。"""
    print(f"\n{CYAN}{BOLD}  Penpot 服务状态{NC}\n")

    # Penpot
    if _is_penpot_running(config):
        ok(f"Penpot Frontend  {DIM}-> http://localhost:{config['penpot_port']}{NC}")
    else:
        fail(f"Penpot Frontend  (端口 {config['penpot_port']} 未监听)")

    # Docker Compose 详情
    compose_file = os.path.join(config["penpot_dir"], "docker-compose.yml")
    if os.path.isfile(compose_file):
        dc_cmd = _docker_compose_cmd()
        if dc_cmd:
            result = run_cmd(
                dc_cmd + ["-f", compose_file, "ps", "--format", "json"],
                cwd=config["penpot_dir"],
                timeout=10,
            )
            if result.returncode == 0 and result.stdout.strip():
                info("Docker 容器状态:")
                # docker compose ps --format json 可能输出多行 JSON
                for line in result.stdout.strip().splitlines():
                    try:
                        svc = json.loads(line)
                        name = svc.get("Name", svc.get("name", "?"))
                        state = svc.get("State", svc.get("state", "?"))
                        status_str = svc.get("Status", svc.get("status", ""))
                        marker = f"{GREEN}OK{NC}" if state == "running" else f"{RED}{state}{NC}"
                        print(f"    [{marker}] {name} {DIM}{status_str}{NC}")
                    except json.JSONDecodeError:
                        pass

    # MCP Server
    if _is_mcp_running(config):
        ok(f"MCP Server       {DIM}-> http://localhost:{config['mcp_port']}/mcp{NC}")
    else:
        fail(f"MCP Server       (端口 {config['mcp_port']} 未监听)")

    pid = _read_mcp_pid()
    if pid:
        alive = _is_process_alive(pid)
        if alive:
            info(f"MCP PID: {pid} (存活)")
        else:
            warn(f"MCP PID: {pid} (已失效，清理 PID 文件)")
            _remove_mcp_pid()

    # Plugin Server
    if is_port_listening(config["plugin_port"]):
        ok(f"Plugin Server    {DIM}-> http://localhost:{config['plugin_port']}{NC}")
    else:
        info(f"Plugin Server    (端口 {config['plugin_port']} 未监听)")

    print()
    return 0


def cmd_ensure(config: dict):
    """快速模式: 已运行则退出，否则启动。"""
    # MCP 已运行
    if _is_mcp_running(config):
        print(f"Penpot MCP already running on port {config['mcp_port']}")
        return 0

    # Penpot 和 MCP 都未运行，尝试启动
    compose_file = os.path.join(config["penpot_dir"], "docker-compose.yml")
    if os.path.isfile(compose_file):
        dc_cmd = _docker_compose_cmd()
        if dc_cmd and not _is_penpot_running(config):
            subprocess.run(
                dc_cmd + ["-f", compose_file, "up", "-d"],
                cwd=config["penpot_dir"],
                capture_output=True,
                timeout=120,
            )
            # 等待 Penpot
            for _ in range(30):
                if _is_penpot_running(config):
                    break
                time.sleep(1)

    # 启动 MCP
    if start_mcp(config):
        return 0

    print(f"Penpot MCP not installed. Run: python {__file__} deploy", file=sys.stderr)
    return 1


